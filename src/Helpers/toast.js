import { toast } from 'react-toastify';

export const successToast = (value) => {
  toast.success(value, {
    position: 'top-right',
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  });
};

